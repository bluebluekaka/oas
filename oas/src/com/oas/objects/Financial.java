package com.oas.objects;

public class Financial extends BaseOrder {

	private Long financialOrderCode;
	
	private Float adjustAmounts;
	
	private Float adjustProfit;
	
	private Integer orderState;
	
	private Float receivedAmount;
	
	private Integer receivedState;
	
	private Integer auditState;

	public Long getFinancialOrderCode() {
		return financialOrderCode;
	}

	public void setFinancialOrderCode(Long financialOrderCode) {
		this.financialOrderCode = financialOrderCode;
	}

	public Float getAdjustProfit() {
		return adjustProfit;
	}

	public void setAdjustProfit(Float adjustProfit) {
		this.adjustProfit = adjustProfit;
	}

	public Integer getOrderState() {
		return orderState;
	}

	public void setOrderState(Integer orderState) {
		this.orderState = orderState;
	}

	public Float getReceivedAmount() {
		return receivedAmount;
	}

	public void setReceivedAmount(Float receivedAmount) {
		this.receivedAmount = receivedAmount;
	}

	public Integer getReceivedState() {
		return receivedState;
	}

	public void setReceivedState(Integer receivedState) {
		this.receivedState = receivedState;
	}

	public Float getAdjustAmounts() {
		return adjustAmounts;
	}

	public void setAdjustAmounts(Float adjustAmounts) {
		this.adjustAmounts = adjustAmounts;
	}

	public Integer getAuditState() {
		return auditState;
	}

	public void setAuditState(Integer auditState) {
		this.auditState = auditState;
	}
	
}
