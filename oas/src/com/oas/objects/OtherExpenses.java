package com.oas.objects;

public class OtherExpenses extends BaseOrder{

	private Long otherExpensesId;
	
	private Long financialOrderCode;
	
	private Integer otherExpensesState;
	
	private Float adjustAmount;
	
	private String amountExplain;
	
	private Integer otherExpensesType;

	public Long getOtherExpensesId() {
		return otherExpensesId;
	}

	public void setOtherExpensesId(Long otherExpensesId) {
		this.otherExpensesId = otherExpensesId;
	}

	public Long getFinancialOrderCode() {
		return financialOrderCode;
	}

	public void setFinancialOrderCode(Long financialOrderCode) {
		this.financialOrderCode = financialOrderCode;
	}

	public Integer getOtherExpensesState() {
		return otherExpensesState;
	}

	public void setOtherExpensesState(Integer otherExpensesState) {
		this.otherExpensesState = otherExpensesState;
	}

	public Float getAdjustAmount() {
		return adjustAmount;
	}

	public void setAdjustAmount(Float adjustAmount) {
		this.adjustAmount = adjustAmount;
	}

	public String getAmountExplain() {
		return amountExplain;
	}

	public void setAmountExplain(String amountExplain) {
		this.amountExplain = amountExplain;
	}

	public Integer getOtherExpensesType() {
		return otherExpensesType;
	}

	public void setOtherExpensesType(Integer otherExpensesType) {
		this.otherExpensesType = otherExpensesType;
	}
	
}
